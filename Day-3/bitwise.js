//Bitwise operator in js
//AND
//OR
//NOT
//XOR
//LEFT SHIFT
//RIGHT SHIFT

const a = 2;
const b = 7;
console.log("A-!-B:", a, b);
console.log("AND:", a & b);
console.log("OR:", a | b);
console.log("NOT:", ~a);
console.log("XOR:", a ^ b);
console.log("LEFT SHIFT:", a << b);
console.log("RIGHT SHIFT:", a >> b);
